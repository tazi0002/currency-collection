function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function createRegularCoin(group, entry) {
  const [year, extraCopies = 0, specialName = "", frontImage = "", backImage = ""] = entry;
  const quantity = 1 + extraCopies;
  const namePrefix = specialName ? `${specialName} ` : "Canadian";

  return {
    id: `canada-coin-${slugify(group.value)}-${slugify(year)}-${slugify(specialName || "regular")}`,
    type: "coin",
    name: `${namePrefix} ${group.value} Coin`,
    country: "Canada",
    year,
    collectionSet: group.collectionSet,
    value: group.value,
    material: group.material,
    condition: "Not listed",
    quantity,
    extraCopies,
    image: frontImage,
    frontImage,
    backImage,
    notes: ""
  };
}

function createSpecialCoin(item, index) {
  return {
    id: `canada-special-${slugify(item.value)}-${slugify(item.year || "unknown")}-${index + 1}`,
    type: "coin",
    name: item.name,
    country: "Canada",
    year: item.year,
    collectionSet: item.collectionSet || "Special Collection",
    value: item.value,
    material: item.material || "Not listed",
    condition: item.condition || "Not listed",
    quantity: 1,
    extraCopies: 0,
    image: item.frontImage || "",
    frontImage: item.frontImage || "",
    backImage: item.backImage || "",
    frontDescription: item.frontDescription || "",
    backDescription: item.backDescription || "",
    notes: "Special collection coin."
  };
}

window.collectionInventory = [
  ...regularCanadianCoins.flatMap((group) => group.items.map((entry) => createRegularCoin(group, entry))),
  ...specialCanadianCoins.map(createSpecialCoin),
  ...banknoteInventory.filter((item) => item.country !== "Canada"),
  ...canadaBanknoteInventory,
  ...worldBanknoteInventory
];



